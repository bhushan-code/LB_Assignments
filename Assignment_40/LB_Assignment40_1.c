void Display()
{
    static int iCnt = 1;
    if(iCnt <= 5)
    {
        printf("*\t");
        iCnt++;
        Display();
    }        
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//This application will display pattern as asked.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}