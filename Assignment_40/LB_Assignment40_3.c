void Display()
{
    static int iCnt = 5;
    if(iCnt >= 1)
    {
        printf("%d\t",iCnt);
        iCnt--;
        Display();
    }        
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//This application will display pattern as asked.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}