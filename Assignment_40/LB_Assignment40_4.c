void Display()
{
    static char ch = 'A';
    if(ch <= 'F')
    {
        printf("%c\t",ch);
        ch++;
        Display();
    }        
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//This application will display pattern as asked.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}